                <h4 class="text-primary">Sejarah Desa</h4>
                <p class="text-abu mt-3">Desa Bulak Kecamatan Jatibarang merupakan desa yang terletak di Wilayah
                    Kabupaten Indramayu, dengan batas sebelah utara dengan Desa Bulak Lor, sebelah Timur dengan Desa
                    Sleman Lor, sebelah Barat dengan Desa Jatibarang dan sebelah Selatan berbatasan dengan Desa
                    Pilangsari.
                    <br>
                    Bicara mengenai Sejarah Desa Bulak, tidak lepas dari keberadaan Situs Buyut Banjar dan keranya yang
                    berjumlah 41 ekor.
                    <br>
                    Sejarah asal usul Desa Bulak dan legenda keranya berawal dari upaya pembuatan atau penggalian kali
                    Prawira Kepolo oleh masyarakat Bulak pada tahun 1800an. Pada saat itu Bulak dipimpin oleh Nyi Sular,
                    anak tunggal dari Ki Laita yang berasal dari Rancajawat hasil perkawinannya dengan Nyi Wana.
                    <br>
                    Nyi Wana adalah seorang wanita yang pertama kali menemukan dan menempati Bulak. Nyi Wana berasal
                    dari Kerajaan Suryanegara yang ditugaskan untuk membangun padepokan baru, membuka pemukinan baru tak
                    jauh dari daerah yang bermata air besar menyerupai sumur dan tak pernah berhenti yang pada saat itu
                    disebut dengan istilah BULAK atau BUBUL kata orang Cikedung, MUMBUL Kata orang Bulak sekarang, atau
                    MULAK (bergejolak) istilah untuk air yang menyembul ke permukaan menurut penyusun.
                    <br>
                    Bersumber pada cerita rakyat, yang disampaikan dari mulut ke mulut. Sasakala Desapun dimulai dan
                    betapa susah kami mencari kebenarannya manakala dari sumber lain ternyata berbeda versi. Dengan ini
                    Kami memohon maaf yang setulus-tulusnya kepada para Sepuh Bulak baik yang masih hidup maupun yang
                    sudah tiada apabila dalam pemaparan legenda ini terdapat kesalahan.
                    <br>
                    Pada jaman dahulu sekitar tahun 1600 sejaman dengan Pangeran Cakra Buana, ada lima kerajaan atau
                    pangeran yang berkuasayaitu:
                    <br>
                <ol class="text-abu">
                    <li>Pagusten Pangeran Suryanegara (dari Cirebon)</li>
                    <li>Pangeran Mangkunegara (adik Pangeran Suryanegara) yang bertempat tinggal di desa Sleman.</li>
                    <li>Pangeran Kartanegara, bertempat tinggal dikampung Karang kendal.</li>
                    <li>Pangeran Martanegara, bertempat tinggal di Gunung Jati.</li>
                    <li>Pangeran Patmanegara, bertempat tinggal di Wanacala (sebelah Timur Cirebon).</li>
                </ol>
                <br>
                Diantara kelima Pangeran itu, Pangeran Suryanegaralah paling berkuasa dan yang paling mempunyai
                kesaktian.Daerah sebelah Barat yang dikuasainya diantaranyayaitu Bulak disamping Daerah Timur yang
                kerapkali kekurangan air.
                <br>
                Untuk upaya pembagian air pada jaman itu, maka Pangeran Suryanegara mengutus Nyi Ayu Kelir dari
                daerah Kedokan supaya bekerja bersama-sama dengan utusan dari kerajaan lainnya untuk membuat
                bendungan di Kali Logangga Tisna.Utusan dari Kedokan berjumlah 41 orang dipimpin oleh Ki Ratim.
                <br>
                Pada suatu hari Sang Pangeran mengecek hasil kerja ke 41 utusan tersebut, yang sebelumnya Pangeran
                menitipkan bungkusan dengan pesan jangan dulu dibuka sebelum Beliau kembali setelah sholat Jum'at.
                Namun betapa kagetnya ketika dijumpainya bungkusan itu sudah terbuka dan 41 utusan memakan isi
                bungkusan yang ternyata buah kurma itu sambil naik di atas pohon.
                <br>
                Melihat kenyataan itu, Sang Pangeran marah karena pesannya tidak dipatuhi dan berucap " Kaya Dudu
                Sifate Menusa, tapi Sifate Kunyuk ! " ( Seperti Bukan Sifat dari Seorang Manusia Tetapi Sifat Hewan
                Monyet ! )
                <br>
                Dengan seijin Allah SWT, seketika ke 41 utusan itu berubah wujud dari manusia menjadi seekor kera.

                Kisah – kisah legenda memiliki nilai – nilai ajaran luhur atau pesan moral yang mesti kita renungkan
                tentunya pendahulu kita ingin memuliakan kita, agar disiplin, amanah, bersatu kompak dan tidak
                seenaknya sendiri agar tidak dikutuk seperti kera.

                Sampai sekarang, konon jumlah kera itu tidak berubah meskipun dijumpai bayi kera atau kera muda yang
                menggelayut di perut induknya sering terlihat oleh warga sekitar.

                Dan belum pernah dijumpai bangkai kera padahal sudah ada kera yang sudah tua dan sakit parah. Secara
                logika umur makhluk hidup itu terbatas pasti akan menemui kematian tetapi hingga sekarang tidak
                sebujurpun bangkai kera ditemukan.

                Anehnya lagi dan perlu mendapat dikaji oleh kita, kera – kera tersebut telah hidup ratusan tahun
                bahkan ribuan tahun yang silam. Tidak hanya kera, pepohonan yang tumbuh di Banjar adalah jenis
                pepohonan hutan belantara seperti pohon Rengas, Keserut, dll. Pohon itupun sama usianya sudah ribuan
                tahun. Bahkan pernah menjadi objek penelitian oleh mahasiswa baik dalam maupun luar Indramayu.

                Berikut ini nama – nama Kuncen atau Juru Kunci Situs Banjar Desa Bulak Kecamatan Jatibarang
                Kabupaten Indramayu :
                <ol class="text-abu">
                    <li>Ki Kadis ( Pada jaman Belanda / VOC)</li>
                    <li>Ki Kajan ( Pada jaman Jepang II)</li>
                    <li>Ki Narim ( Pada jaman PKI)</li>
                    <li>Ki Salkam</li>
                    <li>Ki Kamsari</li>
                </ol>
                Demikian sekilas Sasakala Desa ini, yang kami rangkum setelah dilakukan tanya jawab dengan beberapa
                narasumber.
                </p>
