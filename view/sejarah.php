                <h4 class="text-secondary">Terbentuknya Desa</h4>
                <p class="text-abu mt-3">Sejarah asal usul Desa Bulak secara terperinci tidak bisa dihadirkan di sini,
                    karena perlu investigasi dan penelitian lebih jauh agar benar – benar dapat dipertanggungjawakan.
                    Kata Bulak sendiri memiliki pengertian mata air yang berukuran sangat besar menyerupai sumur yang
                    airnya MULAK ( bergejolak ) sehingga membentuk rawa, entah berkaitan atau tidak karena di sekitar
                    wilayah Bulak banyak ditemukan sumber mata air dan barangkali berhubungan erat dengan keberadaan
                    mata air di daerah lain seperti Balong Tambi, Sumur Gede Kedokan, dsb.
                    Dari tanya jawab dengan beberapa nara sumber para sepuh, diperoleh keterangan atau catatan sejarah
                    sesepuh kampung yang dipercaya oleh masyarakat telah diurai pada Sasakala Desa tersebut di atas.
                    <br>
                    Diawali oleh kepemimpinan Nyi Sular anak semata wayang yang cantik jelita dari pasangan suami istri
                    Ki Laita dan Nyi Wana, yang terpikat asmara dengan pria bernama Suwarja oleh karena ketampanan dan
                    kewibawaannya.
                    Sampai pada akhirnya mereka menikah dan Nyi Sular memutuskan untuk tidak memimpin Daerah Bulak lagi.
                    <br><br>

                    Pada masa kekuasaan Raja Lombok yaitu Anak Agung Gde Jelantik, wilayah Desa Senggigi ( Dusun
                    Mangsit, Kerandangan, Senggigi dan Dusun Loco) masih bergabung dengan Desa Senteluk yang sekarang
                    menjadi Desa Meninting . Sedangkan pada tahun 1962 Desa Senteluk pecah menjadi 2 (Dua) desa yaitu
                    Desa Meninting dan Desa Batulayar dan Dusun Mangsit,Kerandangan,Senggigi dan Dusun Loco bergabung ke
                    Desa Batulayar.
                    <br>
                    Pemberian nama Desa Batulayar pada waktu itu yang lazim disebut dengan Pemusungan/Kepala Dea
                    Batulayar berdasarkan hasil musyawarah nama Batulayar diambil dari nama tempat yang amat terkenal
                    yaitu Makam Batulayar yang sampai saat ini banyak dikunjungi oleh masyarakat Pulau Lombok pada
                    khususnya dan Masyarakat Nusa Tenggara Barat pada umumnya.
                    <br><br>
                    Pada tahun 2001 Desa Batulayar dimekarkan menjadi 2 (dua) yaitu Desa Batulayar (sebagai Desa Induk)
                    dan Desa Senggigi (sebagai Desa Persiapan) dengan SK.Bupati No : 30 Tahun 2001 tanggal 17 Mei 2001,
                    yang pada waktu itu yang menjadi pejabat Kepala Desa Senggigi ialah <b>H. ARIF RAHMAN, S.IP.,</b>
                    dengan jumlah dusun sebanyak 3 dusun, yaitu :
                <ol class="text-abu">
                    <li>Dusun Senggigi</li>

                    <li>Dusun Kerandangan</li>
                    <li>Dusun Mangsit</li>
                </ol>

                Selanjutnya pada tanggal 30 Juli 2003 Pejabat Kepala Desa Senggigi dari <b>H. ARIF RAHMAN, S.IP</b>
                diganti
                oleh Saudara <b>ARIFIN</b> dengan SK. Bupati Lombok Barat No : 409/66/pem/2003. Berhubung Desa Senggigi
                masih bersifat Desa Persiapan, maka berdasarkan hasil musyawarah desa, tertanggal 15 Desember 2003 ,
                maka pada tanggal 22 Desember 2003 Desa Senggigi mengadakan Pemilihan Kepala Desa devinitif yang
                pertama kali dipimpin oleh <b>HAJI JUNAIDI</b> terpilih dengan SK. Bupati Lombok Barat No
                :01/01/Pem/2004
                tertanggal 2 Januari 2004 sampai pada tahun 2008.
                <br>
                Selanjutnya pada tahun 2008, Desa Senggigi mengadakan pemilihan Kepala Desa Senggigi yang kedua dan
                dimenangkan oleh Bapak <b>H. MUTAKIR AHMAD</b> dengan SK. Bupati Lombok Barat No :1320/48/Pem./2008
                tertanggal 23 Desember 2008, Periode 2008-2014. Kemudian Kepala desa terpilih Periode 2015 s/d 2021
                adalah <b>MUHAMMAD ILHAM</b> dengan SK. Bupati Lombok Barat No : 160/04/BPMPD/15 tanggal 27 Januari 2015
                kini baru menjabat 2 (dua) bulan.
                <br><br>
                Demikian selanyang pandang atau sejarah singkat Desa Senggigi yang dapat kami sampaikan kepada para
                pegiat Medsos, semoga dapat bermanfaat untuk kita semua, terima kasih.
